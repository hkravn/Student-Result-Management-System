    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField3.getText();
        String password = convert(jPasswordField1.getPassword());
        
        if (access.equals("admin") && username.equals("admin") && password.equals("pass")) {
            setVisible(false);
            new adminHome().setVisible(true);
        } else if (access.equals("professor") && password.equals(passwd.getPwd(username, 1))) {
            setVisible(false);
            new professorHome(username).setVisible(true);
        } else if (access.equals("student") && password.equals(passwd.getPwd(username, 2))) {
            setVisible(false);
            new studentHome(username).setVisible(true);
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Invalid Password");
            setVisible(false);
            new mainIndex().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

