    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        int sem = jComboBox1.getSelectedIndex()+1;
        
        try {
            Class.forName(sTable.getClassname());
            Connection con = DriverManager.getConnection(sTable.getUrl(),"root",sTable.getPass());
            Statement st = con.createStatement();
            
            String sql = "SELECT * FROM subjects WHERE rollno= '"+rollno+"' AND sem= "+sem;
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()) {
                jLabel2.setText(rs.getString("subj1"));
                jLabel3.setText(rs.getString("subj2"));
                jLabel4.setText(rs.getString("subj3"));
                jLabel5.setText(rs.getString("subj4"));
                jLabel6.setText(rs.getString("subj5"));
                jLabel7.setText(rs.getString("subj6"));
            }
            
            sql = "SELECT * FROM marks WHERE rollno= '"+rollno+"' AND sem= "+sem;
            rs = st.executeQuery(sql);
            
            while (rs.next()) {
                jLabel8.setText(rs.getString("subj1"));
                jLabel9.setText(rs.getString("subj2"));
                jLabel10.setText(rs.getString("subj3"));
                jLabel11.setText(rs.getString("subj4"));
                jLabel12.setText(rs.getString("subj5"));
                jLabel13.setText(rs.getString("subj6"));
            }
            
            con.close();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

