    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String fname = jTextField2.getText();
        
        String address = jTextField4.getText();
        
        try {
            Class.forName(table.getClassname());
            Connection con = DriverManager.getConnection(table.getUrl(),"root",table.getPass());
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select max(empid) from prof");
            
            int empId = 100;
            while (rs.next()) {
                empId = rs.getInt(1)+1;
            }
            
            String val = "("+empId+",'"+name+"','"+fname+"','"+address+"')";
            st.executeUpdate("insert into prof values "+val);
            
            String pass = "('"+empId+"','"+empId+"',"+1+")";
            st.executeUpdate("insert into password values "+pass);
            
            javax.swing.JOptionPane.showMessageDialog(null, "Record Updated. Employee Id: '"+empId+"'");
        } catch(Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, e.toString());
        }
        
        setVisible(false);
        new addProfessor().setVisible(true);
    }//GEN-LAST:event_jButton6ActionPerformed

