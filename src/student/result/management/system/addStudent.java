    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int course = jComboBox1.getSelectedIndex()+1;
        int stream = jComboBox2.getSelectedIndex()+1;
        
        String name = jTextField1.getText();
        String fname = jTextField2.getText();
        
        int dd = jComboBox3.getSelectedIndex()+1;
        int mm = jComboBox4.getSelectedIndex()+1;
        int yy = jComboBox5.getSelectedIndex()+1990;
        
        String gender = (String)jComboBox6.getSelectedItem();
        String address = jTextField3.getText();
        
        //Create new roll
        String roll = 2100 + course*10 + stream + "";
        
        try {
            Class.forName(table.getClassname());
            Connection con = DriverManager.getConnection(table.getUrl(),"root",table.getPass());
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select max(last) from students where rollno like '"+roll+"%'");
            
            int lastroll = 1;
            while (rs.next()) {
                lastroll = rs.getInt(1)+1;
            }
            
            roll = roll + table.getHm(stream) + lastroll;
            String val = "('"+roll+"',"+lastroll+",'"+name+"','"+fname+"','"+dd+"/"+mm+"/"+yy+"','"+gender+"','"+address+"')";
            String sql = "insert into students values "+val;
            
            st.executeUpdate(sql);
            for (int i=1;i<=8;i++) {
                sql = "insert into marks (rollno,sem) values ('"+roll+"',"+i+")";
                st.executeUpdate(sql);
            }
            
            String pass = "('"+roll+"','"+roll+"',"+2+")";
            st.executeUpdate("insert into password values "+pass);
            
            javax.swing.JOptionPane.showMessageDialog(null, "Record Updated. Roll No.: '"+roll+"'");
        } catch(Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, e.toString());
        }
        
        setVisible(false);
        new addStudent().setVisible(true);
    }//GEN-LAST:event_jButton6ActionPerformed

