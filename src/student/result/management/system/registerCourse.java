    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int sem = jComboBox1.getSelectedIndex()+1;
        
        String subj1 = jTextField1.getText();
        String subj2 = jTextField2.getText();
        String subj3 = jTextField3.getText();
        String subj4 = jTextField4.getText();
        String subj5 = jTextField5.getText();
        String subj6 = jTextField6.getText();
        
        try {
            Class.forName(table.getClassname());
            Connection con = DriverManager.getConnection(table.getUrl(),"root",table.getPass());
            
            Statement st = con.createStatement();
            String sql = "('"+rollno+"',"+sem+",'"+subj1+"','"+subj2+"','"+subj3+"','"+subj4+"','"+subj5+"','"+subj6+"')";
            
            sql = "insert into subjects values "+sql;
            st.executeUpdate(sql);
            
            javax.swing.JOptionPane.showMessageDialog(null, "Records Updated!!!");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, e.toString());
        }
        
        setVisible(false);
        new registerCourse(rollno).setVisible(true);
    }//GEN-LAST:event_jButton4ActionPerformed

